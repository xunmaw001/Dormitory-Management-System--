            susheOptions: [],//宿舍
            susheIndex : 0,//宿舍下标

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
            let susheParams = {
                page: 1,
                limit: 100,
            }
            let susheData = await this.$api.page(`sushe`, susheParams);
            this.susheOptions = susheData.data.list;
		/*下拉框*/
			let susheweishengTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'susheweisheng_types',
            }
			let susheweishengTypes = await this.$api.page(`dictionary`, susheweishengTypesParams);
			this.susheweishengTypesOptions = susheweishengTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`susheweisheng`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.susheweishengId){
                this.ruleForm.susheweishengId = options.susheweishengId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            susheChange(e) {
                this.susheIndex = e.target.value
                this.ruleForm.susheName = this.susheOptions[this.susheIndex].susheName
                this.ruleForm.susheId = this.susheOptions[this.susheIndex].id
            },
            // 下拉变化
            susheweishengTypesChange(e) {
                this.susheweishengTypesIndex = e.target.value
                this.ruleForm.susheweishengValue = this.susheweishengTypesOptions[this.susheweishengTypesIndex].indexName
                this.ruleForm.susheweishengTypes = this.susheweishengTypesOptions[this.susheweishengTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			// 日期控件
			riqiTimeConfirm(val) {
                this.ruleForm.riqiTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.susheweishengUuidNumnber)) {
                    this.$utils.msg(`宿舍卫生编号不能为空`);
                    return
                }
				if ((!this.ruleForm.dafen) && (!this.$validate.isNumber(this.ruleForm.dafen)) && this.ruleForm.dafen >0) {
                    this.$utils.msg(`打分不能为空，不能小于0 格式为数字或小数`);
                    return
                }
				if ((!this.ruleForm.riqiTime)) {
                    this.$utils.msg(`所属日期不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`susheweisheng`, this.ruleForm);
                } else {
                    await this.$api.save(`susheweisheng`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
