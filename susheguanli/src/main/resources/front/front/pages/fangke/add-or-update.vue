					fangkeContent: '',
					insertTime: '',
					createTime: '',
            },
            // 登陆用户信息
            user: {},
            sexTypesOptions: [],
            sexTypesIndex : 0,
            xueshengOptions: [],//学生
            xueshengIndex : 0,//学生下标

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
            let xueshengParams = {
                page: 1,
                limit: 100,
            }
            let xueshengData = await this.$api.page(`xuesheng`, xueshengParams);
            this.xueshengOptions = xueshengData.data.list;
		/*下拉框*/
			let sexTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'sex_types',
            }
			let sexTypes = await this.$api.page(`dictionary`, sexTypesParams);
			this.sexTypesOptions = sexTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`fangke`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.fangkeId){
                this.ruleForm.fangkeId = options.fangkeId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            xueshengChange(e) {
                this.xueshengIndex = e.target.value
                this.ruleForm.xueshengName = this.xueshengOptions[this.xueshengIndex].xueshengName
                this.ruleForm.xueshengId = this.xueshengOptions[this.xueshengIndex].id
            },
            // 下拉变化
            sexTypesChange(e) {
                this.sexTypesIndex = e.target.value
                this.ruleForm.sexValue = this.sexTypesOptions[this.sexTypesIndex].indexName
                this.ruleForm.sexTypes = this.sexTypesOptions[this.sexTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
            // 		//   el.style.backgroundColor = this.addUpdateForm.yaoxianStyle.content.backgroundColor
            // 		// })
            // 	})
            // },
			fangkePhotoTap() {
                let _this = this;
                this.$api.upload(function(res) {
                    _this.ruleForm.fangkePhoto = 'upload/' + res.file;
                    _this.$forceUpdate();
                    _this.$nextTick(() => {
                        // _this.styleChange()
                    })
                });
            },
			// 日期控件
			insertTimeConfirm(val) {
                this.ruleForm.insertTime = val.result;
                this.$forceUpdate();
            },
			// 日期控件
			createTimeConfirm(val) {
                this.ruleForm.createTime = val.result;
                this.$forceUpdate();
            },

            getUUID() {
                return new Date().getTime();
            },
            async onSubmitTap() {
				if ((!this.ruleForm.fangkeName)) {
                    this.$utils.msg(`访客姓名不能为空`);
                    return
                }
				if ((!this.ruleForm.fangkePhone) && (!this.$validate.isMobile(this.ruleForm.fangkePhone))) {
                    this.$utils.msg(`访客手机号不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.fangkeIdNumber) && (!this.$validate.checkIdCard(this.ruleForm.fangkeIdNumber))) {
                    this.$utils.msg(`访客身份证号不能为空并且需要输入正确格式`);
                    return
                }
				if ((!this.ruleForm.sexTypes)) {
                    this.$utils.msg(`性别不能为空`);
                    return
                }
				if ((!this.ruleForm.fangkePhoto)) {
                    this.$utils.msg(`健康码不能为空`);
                    return
                }
                if (this.ruleForm.id) {
                    await this.$api.update(`fangke`, this.ruleForm);
                } else {
                    await this.$api.save(`fangke`, this.ruleForm);
                }
                uni.setStorageSync('pingluenStateState', true);
                this.$utils.msgBack('提交成功');
            },
            getDate(type) {
                const date = new Date();
                let year = date.getFullYear();
                let month = date.getMonth() + 1;
                let day = date.getDate();
                if (type === 'start') {
                    year = year - 60;
                } else if (type === 'end') {
                    year = year + 2;
                }
                month = month > 9 ? month : '0' + month;;
                day = day > 9 ? day : '0' + day;
                return `${year}-${month}-${day}`;
            },
            toggleTab(str) {
                this.$refs[str].show();
            }
        }
    }
</script>
