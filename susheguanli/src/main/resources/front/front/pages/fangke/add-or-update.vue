<script>
    import wPicker from "@/components/w-picker/w-picker.vue";

    export default {
        data() {
            return {
                cross: '',
                ro:{
					xueshengId: false,
					fangkeName: false,
					fangkePhone: false,
					fangkeIdNumber: false,
