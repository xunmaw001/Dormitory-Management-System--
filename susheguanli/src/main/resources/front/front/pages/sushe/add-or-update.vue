					insertTime: false,
					createTime: false,
        },
            ruleForm: {
					susheUuidNumber: this.getUUID(),//数字
					loudongTypes: '',//数字
					loudongValue: '',//数字对应的值
					danyuanTypes: '',//数字
					danyuanValue: '',//数字对应的值
					susheName: '',
					susheAddress: '',
					susheContent: '',
					insertTime: '',
					createTime: '',
            },
            // 登陆用户信息
