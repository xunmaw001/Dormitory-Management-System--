            loudongTypesOptions: [],
            loudongTypesIndex : 0,
            danyuanTypesOptions: [],
            danyuanTypesIndex : 0,

        }
        },
        components: {
            wPicker
        },
        computed: {
            baseUrl() {
                return this.$base.url;
            },
        },
        async onLoad(options) {
		/*下拉框*/
			let loudongTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'loudong_types',
            }
			let loudongTypes = await this.$api.page(`dictionary`, loudongTypesParams);
			this.loudongTypesOptions = loudongTypes.data.list
		/*下拉框*/
			let danyuanTypesParams = {
                page: 1,
                limit: 100,
                dicCode: 'danyuan_types',
            }
			let danyuanTypes = await this.$api.page(`dictionary`, danyuanTypesParams);
			this.danyuanTypesOptions = danyuanTypes.data.list


            // 如果是更新操作
            if (options.id) {
                this.ruleForm.id = options.id;
                // 获取信息
                let res = await this.$api.info(`sushe`, this.ruleForm.id);
                this.ruleForm = res.data;
            }
            if(options.susheId){
                this.ruleForm.susheId = options.susheId;
            }
            // 跨表
            // this.styleChange()
        },
        methods: {
            // 下拉变化
            loudongTypesChange(e) {
                this.loudongTypesIndex = e.target.value
                this.ruleForm.loudongValue = this.loudongTypesOptions[this.loudongTypesIndex].indexName
                this.ruleForm.loudongTypes = this.loudongTypesOptions[this.loudongTypesIndex].codeIndex
            },
            danyuanTypesChange(e) {
                this.danyuanTypesIndex = e.target.value
                this.ruleForm.danyuanValue = this.danyuanTypesOptions[this.danyuanTypesIndex].indexName
                this.ruleForm.danyuanTypes = this.danyuanTypesOptions[this.danyuanTypesIndex].codeIndex
            },
            // styleChange() {
            // 	this.$nextTick(() => {
            // 		// document.querySelectorAll('.app-update-pv .cu-form-group .uni-yaoxianStyle-yaoxianStyle').forEach(el=>{
